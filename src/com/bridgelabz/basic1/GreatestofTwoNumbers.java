package com.bridgelabz.basic1;

public class GreatestofTwoNumbers {

	public static void main(String[] args) 
	{
		int num1 = 50;
		int num2 = 20;
		if(num1 == num2)
			System.out.println("Both are equal");
		else if (num1 > num2)
			System.out.println(num1 + " is greater");
		else
			System.out.println(num2 + " is greater");
	
	}

}
