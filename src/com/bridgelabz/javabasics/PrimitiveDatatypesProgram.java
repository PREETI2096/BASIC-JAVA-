package com.bridgelabz.javabasics;

public class PrimitiveDatatypesProgram {
	
	static byte b;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	static char ch;
	static boolean bu;

	public static void main(String[] args) {
		System.out.println("default value of byte is : " + b);
		System.out.println("default value of short is : " + s);
		System.out.println("default value of integer is : " + i);
		System.out.println("default value of long is : " + l+"L");
		System.out.println("default value of float is : " + f+ "f");
		System.out.println("default value of double is : " + d+"d");
		System.out.println("default value of char is : " + ch);
		System.out.println("default value of boolean is : " + bu);
	}

}
